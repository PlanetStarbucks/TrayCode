const run = function (input) {
	// a function that will return an entire MP construction object from the Global Schema
	const getMetaproperty = function (key) {
		let res = input.globalMetaConfig[key].metaproperty;
		delete res.zindex;
		return res;
	};

	// a function that will return an entire options construction object from the Global Schema
	const getOptions = function (key) {
		let res = input.globalMetaConfig[key].options;
		return res;
	};

	// a function to lookup the correct label of a given key (MP Name)
	const getEdit = function (key) {
		// lookup the correct display name from the global schema
		const displayName = input.globalNamingConfig.filter((el) => {
			return el.name === key;
		})[0];

		// check if the config has marked that an MP is required
		const required = input.configMetaproperties.filter((el) => {
			return el.key === key;
		})[0];

		// return an object with label and zindex.  The label is the name from the schema and if required
		// include that in the lable string
		let res = {
			label: `${displayName.label}${required.value === "required" ? " (required for sync)" : ""}`,
			zindex: 91,
		};
		return res;
	};

	// Create an object to hold all the states of MP edits we might need to make.  Exist holds an array
	// of objects that are already created in the DAM.  Create holds an array to be created by the workflow.
	// Update holds an array in which the names have been changed (the workflow will reset the names on ever run,
	// so that if a user switches an option from required to optional or vice versa, the MP labels will be updated)
	let res = {
		exist: [],
		create: [],
		update: [],
	};

	// loop through the config MP input and check the state of it against the current state of MPs in Bynder.
	for (let i = 0; i < input.configMetaproperties.length; i++) {
		// pull out the key for easier use in the function, and get the edit option for that key
		let mpName = input.configMetaproperties[i].key;
		let edit = getEdit(mpName);

		// check if the MP identified by the user in config exists in the current Bynder taxonomy.  If not, add the
		// construction object to the create array.  If not, add the name to the exist array.
		if (input.bynderMetaproperties[mpName] === undefined) {
			res.create.push({
				metaproperty: getMetaproperty(mpName),
				options: getOptions(mpName),
				edit: edit,
			});
		} else {
			res.exist.push(mpName);

			// check if the label in the Bynder account matches what it should be from the edit object.  If not, the names
			// should be updated in the WF.  Returns back the proper label, and the MP id to be edited.
			if (edit.label !== input.bynderMetaproperties[mpName].label) {
				res.update.push({
					id: input.bynderMetaproperties[mpName].id,
					label: edit.label,
				});
			}
		}
	}
	return res;
};

